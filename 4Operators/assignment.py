# Assignment Operators in Python
a = 10 # Assign 10 to a (Answer:10)
print(a)
a +=5 # a = a + 5 (Answer:15)
print(a)
a -= 6 # a = a - 6 (Answer:9)
print(a)
a *= 5 # a = a * 5 (Answer:45)
print(a)
a /= 2 # a = a / 2 (Answer:22.5)
print(a)
a %= 3 # a = a % 3 (Answer:1.5)
print(a)
a **=5 # a = a ** 5 (Answer:7.59375)
print (a)
a //=4 # a = a // 4 (Answer:1.0)
print(a)