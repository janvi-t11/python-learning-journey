# Membership Operators in Python
A = "hi there"
print("hi" in A) # True because "hi" is present in A
print("hello" not in A) # True because "hello" is not present in A
print("h" in A) # True because "h" is present in A


x = ["J","A","N","V","I"]
print("J" in x) # True because "J" is present in list x
print("Z" not in x) # True because "Z" is not present in list x